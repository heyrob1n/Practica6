package practica6;
import java.util.Scanner;


public class scan {
	static Scanner scan = new Scanner(System.in);
	
	public static String String() {
		return scan.nextLine();	
	}
	
	public static int Int() {
		return scan.nextInt();	
	}
	
	public static double Double() {
		return scan.nextDouble();	
	}
		
}
