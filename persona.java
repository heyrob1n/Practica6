package practica6;


	public class persona {

	     int edad;
	     String nombre;
	     int telefono;
	     
	     public persona(){
	     }
	     
	     public String getNombre(){
	         return nombre;   
	     }
	     
	     public void setNombre(String name){
	         nombre=name;
	     }
	     
	     public int getTelefono(){
	         return telefono;   
	     }
	     
	     public void setTelefono(int tel){
	         telefono=tel;
	     }
	     
	     public int getEdad(){
	         return edad;   
	     }
	     
	     public void setEdad(int age){
	         edad=age;
	     }

	
}
